
package br.senai.sc.sisges.modelo;


public class Equipe {
    
    private long idEqu;
    private String nomEqu;
    private String desEqu;

    public long getIdEqu() {
        return idEqu;
    }

    public void setIdEqu(long idEqu) {
        this.idEqu = idEqu;
    }

    public String getNomEqu() {
        return nomEqu;
    }

    public void setNomEqu(String nomEqu) {
        this.nomEqu = nomEqu;
    }

    public String getDesEqu() {
        return desEqu;
    }

    public void setDesEqu(String desEqu) {
        this.desEqu = desEqu;
    }
    
    
    
}
